import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const root = path.resolve(new URL("..", import.meta.url).pathname.replace(/^\/([A-Za-z]:)/, "$1"));
const workspace = path.resolve(root, "..");
const inventoryPath = path.join(workspace, "reports", "post-inventory.json");
const generatedDir = path.join(root, "src", "data", "generated");
const outJsonPath = path.join(workspace, "reports", "post-migration-ledger.json");
const outMdPath = path.join(workspace, "reports", "post-migration-ledger.md");

const generatedFiles = [
  ["admission-info", "admissionInfoPosts.json"],
  ["university-entrance-strategy", "universityStrategyPosts.json"],
  ["interview-prep-guide", "interviewPrepPosts.json"],
  ["voice-interview", "voiceInterviewPosts.json"],
];

async function readJson(filePath, fallback) {
  try {
    return JSON.parse(await readFile(filePath, "utf8"));
  } catch {
    return fallback;
  }
}

function byTemplateAndYear(rows) {
  const summary = {};
  for (const row of rows) {
    const template = row.template || row.recommendedTemplate || "unknown";
    const year = (row.date || "unknown").slice(0, 4) || "unknown";
    summary[template] ??= { total: 0, byYear: {} };
    summary[template].total += 1;
    summary[template].byYear[year] = (summary[template].byYear[year] || 0) + 1;
  }
  return summary;
}

const inventory = await readJson(inventoryPath, { records: [] });
const generatedByPath = new Map();
const generatedSummary = {};

for (const [template, fileName] of generatedFiles) {
  const rows = await readJson(path.join(generatedDir, fileName), []);
  generatedSummary[template] = rows.length;
  for (const post of rows) {
    generatedByPath.set(post.path, {
      slug: post.slug,
      template: post.template,
      generatedTitle: post.displayTitle || post.title,
      heroVariant: post.heroVariant || "standard",
      htmlBytes: post.sourceMetrics?.htmlBytes || 0,
      tocCount: post.sourceMetrics?.tocCount || 0,
      imageCount: post.sourceMetrics?.imageCount || 0,
    });
  }
}

const rows = (inventory.records || []).map((record) => {
  const generated = generatedByPath.get(record.path);
  const status = generated ? "generated" : record.alreadyInStaticBaseline ? "static-baseline" : "pending";
  return {
    id: record.id,
    slug: record.slug,
    path: record.path,
    url: record.url,
    title: record.title,
    date: record.date,
    modified: record.modified,
    categories: record.categories || [],
    recommendedTemplate: record.recommendedTemplate || "unknown",
    migrationComplexity: record.migrationComplexity || "unknown",
    status,
    generatedTemplate: generated?.template || null,
    generatedTitle: generated?.generatedTitle || null,
    heroVariant: generated?.heroVariant || null,
    htmlBytes: generated?.htmlBytes || 0,
    tocCount: generated?.tocCount || 0,
    imageCount: generated?.imageCount || 0,
    htmlStatus: record.htmlStatus || null,
    textLength: record.htmlAudit?.textLength || 0,
    tableCount: record.htmlAudit?.tableCount || 0,
    videoCount: record.htmlAudit?.videoUrls?.length || 0,
    wpUploadAssetCount: record.htmlAudit?.wpUploadAssetCount || 0,
  };
});

const summary = {
  generatedAt: new Date().toISOString(),
  sourceRecords: rows.length,
  status: rows.reduce((acc, row) => {
    acc[row.status] = (acc[row.status] || 0) + 1;
    return acc;
  }, {}),
  inventoryByTemplateAndYear: byTemplateAndYear(rows.map((row) => ({ ...row, template: row.recommendedTemplate }))),
  generatedByTemplate: generatedSummary,
};

const priorityTemplates = new Set([
  "admission-info",
  "university-entrance-strategy",
  "interview-prep-guide",
  "voice-interview",
]);
const pendingPriorityRows = rows
  .filter((row) => priorityTemplates.has(row.recommendedTemplate) && row.status !== "generated")
  .sort((a, b) => (b.date || "").localeCompare(a.date || ""));

await mkdir(path.dirname(outJsonPath), { recursive: true });
await writeFile(outJsonPath, `${JSON.stringify({ summary, rows }, null, 2)}\n`, "utf8");

const md = [
  "# Post Migration Ledger",
  "",
  `Generated: ${summary.generatedAt}`,
  "",
  "## Summary",
  "",
  `- Source records: ${summary.sourceRecords}`,
  `- Generated: ${summary.status.generated || 0}`,
  `- Static baseline only: ${summary.status["static-baseline"] || 0}`,
  `- Pending: ${summary.status.pending || 0}`,
  "",
  "## Generated By Template",
  "",
  "| Template | Generated |",
  "| --- | ---: |",
  ...Object.entries(summary.generatedByTemplate).map(([template, count]) => `| ${template} | ${count} |`),
  "",
  "## Pending Priority Records",
  "",
  "| Path | Template | Date | Title |",
  "| --- | --- | --- | --- |",
  ...pendingPriorityRows
    .slice(0, 80)
    .map((row) => `| ${row.path} | ${row.recommendedTemplate} | ${row.date || ""} | ${String(row.title).replace(/\|/g, "｜")} |`),
  "",
];

await writeFile(outMdPath, `${md.join("\n")}\n`, "utf8");

console.log(`Ledger rows: ${rows.length}`);
console.log(`Generated: ${summary.status.generated || 0}`);
console.log(`Pending: ${summary.status.pending || 0}`);
console.log(`Output: ${outJsonPath}`);
